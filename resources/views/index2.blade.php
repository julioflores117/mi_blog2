								<p>
								Lógica de acceso: se utilizan para realizar la autenticación del usuario y sirven para proteger los datos y aplicaciones <br>
Negocio de datos:
Ahora que está tan de moda el «Big Data» y las empresas (sobre todo las grandes) se obsesionan por recoger datos, es un buen momento para pararse a pensar en cómo rentabilizar esos datos.
aparecen ocho temas para tener en cuenta: <br>
Recoger y suministrar: se trata de la recogida de datos y su venta sin procesar. <br>
Almacenar y hospedar: consistiría en disponer datos para terceros, con la ventaja de poder aplicar economías de escala si conseguimos diferentes usuarios para los mismos datos. <br>
Filtrar y refinar: la limpieza y depuración de datos es un trabajo poco gratificante pero necesario en cualquier proceso de análisis de datos. Si en lugar de vender datos «en bruto¨ eres capaz de conseguir clientes que estén dispuestos a pagar un plus por disponer de datos sin duplicados, errores y otros tipos de defectos, tienes entre manos un negocio más lucrativo. <br>
Mejorar y enriquecer: añadir valor a un conjunto de datos, combinándolo con otro o llevando a cabo un pre-procesado redundaría en datos listos para el usuario final. <br>
Simplifica el acceso: ayuda a los usuarios de negocio a escoger los datos que necesitan, en el formato que más les interese. Esto puede ser tan simple como convertir los datos a otro formato más conveniente para el usuario (de PDF a CSV) o tan complejo como crear un sistema que permita a los usuarios escoger sub-segmentos de datos, más acordes a sus propias necesidades. <br>
Analiza: descubre nueva información para guiar las decisiones de negocio o explora los datos para contestar preguntas. <br>
Oculta: durante años ha resultado cada vez más fácil la recogida de datos, pero no todo el mundo quiere formar parte de los ejercicios de análisis. Hay profesionales que basan su negocio en ayudar a ciertas personas y empresas a mantener sus datos apartados de miradas indiscretas. <br>
Ofrece consejo/consultoría: los datos son ingredientes clave en muchos negocios. Los consultores especializados ayudan a las empresas a obtener el máximo rendimiento de sus datos, desde cómo analizarlos a cómo monetizarlos. <br>


