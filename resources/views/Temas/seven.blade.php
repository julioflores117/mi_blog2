<p>Los protocolos para la transmisión de datos en internet más importantes son TCP (Protocolo de Control de Transmisión) e IP (Protocolo de Internet).</p>
<p>Existen varios tipos de protocolos de red: Protocolos de comunicación de red: protocolos de comunicación de paquetes básicos como TCP / IP y HTTP. Protocolos de seguridad de red: implementan la seguridad en las comunicaciones de red entre servidores, incluye HTTPS, SSL y SFTP. Protocolos de gestión de red: proporcionan mantenimiento y gobierno de red, incluyen SNMP e ICMP.</p>
