										<h2><a href="#">Tipos de servidores</a></h2>
										<p></p>
									</div>
									<div class="meta">
										<time class="published" datetime="2015-11-01">November 1, 2015</time>
										<a href="#" class="author"><span class="name">Julio Flores Pineda</span><img src="images/avatar.jpg" alt="" /></a>
									</div>
								</header>
								<span class="image featured"><img src="images/p1.jpg" alt="" /></span>
                                <P>
                                Básicamente un servidor es un dispositivo o infraestructura informática que hace parte de una red y provee información a otros servidores. Esta información puede transmitirse de ordenador a personas y es de diferentes tipos: <br>
                                1. Servidor de email <br>
Uno de los más antiguos que encontraremos en esta lista. Funciona como una especie de oficina de correo para almacenar, recibir, enviar y permitir múltiples operaciones que tienen que ver con el correo personal de los clientes. <br>
2. Servidor web <br>
Un servidor web se ocupa de guardar la información en formato HTML de los sitios, donde se incluye texto, imágenes, videos y todo tipo de datos. Mediante un explorador web, los usuarios puedan visualizar todo esto en sus pantallas. <br>
3. Servidor de base de datos <br>
Son dispositivos diseñados para almacenar grandes cúmulos de información y poder gestionar los datos uno por uno. <br>
4. Servidores cloud <br>
Estos sirven para compañías que se dedican a rentar un espacio en sus servidores para que otras personas o empresas guarden la información de manera remota. Sirven para almacenar grandes cantidades de datos y así proteger la información de las organizaciones o personas naturales. <br>
5. Servidor DNS <br>
Están encargados de gestionar los nombres de los dominios de las páginas web. Es decir, su trabajo es crear un vínculo entre el dominio del sitio con su IP <br>
6. Servidor Telnet <br>
Utilizado principalmente en las telecomunicaciones, es un protocolo de red que le permite a los usuarios gestionar, enviar y recibir datos para solucionar problemas con las redes relacionadas con la telefonía. <br>
7. Servidor SIP <br>
Es conocido como Proxy SIP y su trabajo es establecer la conexión para llamadas telefónicas por Internet. El mismo no transmite audio ni video. <br>
8. Servidor FTP <br>
Es un servidor que está conectado a Internet que permite la posibilidad de transferir archivos y datos entre otros ordenadores y servidores. <br>
9. Servidor del acceso remoto (RAS) <br>
Vigila las líneas de módem de los ordenadores u otros medios de comunicación de Internet, de manera tal que los requerimientos conecten con la red en forma remota, contesta llamadas e interviene la petición de la red. <br>
10. Servidor compartido <br>
Otorgan espacio para guardar información y al mismo tiempo comparte recursos con la memoria RAM, el CPU, el sistema operativo, la conectividad a Internet y la dirección IP. <br>
                                </P>
