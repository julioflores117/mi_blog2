										<h2><a href="#">Definir los conceptos de Infrastructure as a Service (IAAS), Platform as a Service (PAAS), Software as a Service (SAAS)</a></h2>
										<p>Componentes y características del Cliente/Servidor</p>
									</div>
									<div class="meta">
										<time class="published" datetime="2015-11-01">November 1, 2015</time>
										<a href="#" class="author"><span class="name">Julio Flores Pineda</span><img src="images/avatar.jpg" alt="" /></a>
									</div>
								</header>
								<span class="image featured"><img src="images/mod-cliente-servidor.jpg" alt="" /></span>
                               <p>
                               IAAS <br>
Con la infraestructura como servicio, los usuarios acceden a lo básico de la infraestructura como el espacio en servidor, almacenamiento de datos y conectividad de red, que pueden proporcionarse por medio de una API. 
Los administradores del sistema dentro de la empresa son responsables de gestionar las bases de datos de la empresa, las aplicaciones, las medidas de seguridad y otros factores, mientras que la computación en la nube gestiona los servidores, discos duros, conectividad de red y almacenamiento. <br>
PAAS <br>
La plataforma como servicio (PaaS) ofrece un entorno de desarrollo completo, eliminando la necesidad de que los desarrolladores se ocupen directamente de la capa de infraestructura al implementar o actualizar aplicaciones. Además de los elementos de la infraestructura como servicio, la Plataforma 
como servicio incluye diversas herramientas y software que los desarrolladores necesitan para construir, probar y ejecutar sus aplicaciones. <br>
SAAS <br>
El software como servicio (SaaS) es la categoría que conoce probablemente la mayoría de los usuarios. Las aplicaciones SaaS están diseñadas directamente para usuarios finales, mientras que la infraestructura subyacente se que queda en un segundo plano.
 Los usuarios en general acceden al servicio por medio de un navegador web o aplicación, y la facturación habitualmente se lleva a cabo por usuario o asiento.
                               </p>
