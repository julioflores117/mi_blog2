								<span class="image featured"><img src="images/mod-cliente-servidor.jpg" alt="" /></span>
                               <p>
                               Ventajas de la arquitectura cliente-servidor <br>
•	La rentabilidad necesita menos costes de mantenimiento y la recuperación de datos es posible. <br>
•	La capacidad de los clientes / servidores se cambiará por separado.<br>
•	Sistema centralizado con datos en un solo lugar.<br>
Desventajas de la arquitectura cliente-servidor: <br>
•	Los clientes pueden ser propensos a virus, gusanos y troyanos si están presentes en un servidor / cargados en el servidor.<br>
•	El servidor puede ser propenso a ataques de denegación de servicio.<br>
•	Los paquetes de datos se modificarán o falsificarán durante la transmisión.<br>
•	El phishing y la captura de credenciales de inicio de sesión y otra información útil del usuario son bastante comunes y el ataque Man in a Middle será común.<br>
                               </p>
