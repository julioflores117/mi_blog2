								<span class="image featured"><img src="images/mod-cliente-servidor.jpg" alt="" /></span>
                                <p>
                                Un modelado Cliente/Servidor es un Sistema de Información distribuido basado en las siguientes elementos: <br>
                                CLIENTE <br>
                                Un cliente es todo proceso que reclama servicios de otro. Una definición un poco más elaborada podría ser la siguiente: cliente es el proceso que permite al usuario formular los requerimientos y pasarlos al servidor. Se lo conoce con el término front-end. <br>
                                SERVIDOR <br>
Un servidor es todo proceso que proporciona un servicio a otros. Es el proceso encargado de atender a múltiples clientes que hacen peticiones de algún recurso administrado por él. Al proceso servidor se lo conoce con el término back-end. El servidor normalmente maneja todas las funciones relacionadas con la mayoría de las reglas del negocio y los recursos de datos. <br>
MIDDLEWARE <br>
El middleware es un módulo intermedio que actúa como conductor entre sistemas permitiendo a cualquier usuario de sistemas de información comunicarse con varias fuentes de información que se encuentran conectadas por una red. En el caso que nos concierne, es el intermediario entre el cliente y el servidor y se ejecuta en ambas partes. <br>
COMUNICACIÓN ENTRE LOS ELEMENTOS (NOS) <br>
Como se ha comentado en el apartado anterior, el middleware es un conjunto de aplicaciones encargadas de enlazar al cliente con el servidor. <br>

                                </p>
